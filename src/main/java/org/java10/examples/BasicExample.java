package org.java10.examples;

public class BasicExample {

    public static void main(String[] args) {
        var b = false;
        var y = (byte)10;
        var c = 'A';
        var s = (short)100;
        var i = 0;
        var l = 999l;
        var f = 3.14f;
        var d = 1.61803398874d;

        System.out.println("Printing a boolean (var b = false)");
        System.out.println(b);
        System.out.println("Printing a byte (var y = (byte)10)");
        System.out.println(y);
        System.out.println("Printing a char (var c = 'A')");
        System.out.println(c);
        System.out.println("Printing a short (var s = (short)100)");
        System.out.println(s);
        System.out.println("Printing a int (var i = 0)");
        System.out.println(i);
        System.out.println("Printing a long (var l = 999l)");
        System.out.println(l);
        System.out.println("Printing a float (var f = 3.14f)");
        System.out.println(f);
        System.out.println("Printing a double (var d = 1.61803398874d)");
        System.out.println(d);
    }
}
